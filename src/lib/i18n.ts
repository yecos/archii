/** All translation dictionaries indexed by locale */
export const DICTIONARIES: Record<Locale, TranslationDict> = { es, en, pt };

/**
 * Translate a key with optional interpolation.
 * Usage: t('common.items', { count: 5 }) → "5 items"
 *        t('common.of') → "of"
 */
export function translate(key: string, locale: Locale, params?: Record<string, string | number>): string {
  const dict = DICTIONARIES[locale] || DICTIONARIES[DEFAULT_LOCALE];
  let text = dict[key] || DICTIONARIES[DEFAULT_LOCALE][key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return text;
}

/**
 * Get translated arrays (for months, weekdays etc.)
 */
export function getTranslatedArray(key: string, locale: Locale): string[] {
  const text = translate(key, locale);
  return text.split(',');
}

/**
 * Detect locale from browser settings
 */
export function detectBrowserLocale(): Locale {
  if (typeof navigator === 'undefined') return DEFAULT_LOCALE;
  const lang = navigator.language || (navigator as unknown as Record<string, string>).userLanguage || '';
  const code = lang.toLowerCase().split('-')[0];
  if (code === 'en') return 'en';
  if (code === 'pt') return 'pt';
  return DEFAULT_LOCALE; // Default to Spanish
}
